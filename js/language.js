// Current language (default: English)
let currentLanguage = localStorage.getItem('tixbroLanguage') || 'en';

// Function to change language
function changeLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('tixbroLanguage', lang);

    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            // Check if element has a placeholder attribute
            if (element.hasAttribute('placeholder')) {
                element.placeholder = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });

    // Update language button text
    const langBtn = document.getElementById('currentLang');
    if (langBtn) {
        langBtn.textContent = lang === 'en' ? 'English' : 'हिन्दी';
    }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    // Set initial language
    changeLanguage(currentLanguage);

    // Add click event to language toggle button
    const languageToggle = document.getElementById('languageToggle');
    if (languageToggle) {
        languageToggle.addEventListener('click', function() {
            const newLang = currentLanguage === 'en' ? 'hi' : 'en';
            changeLanguage(newLang);
        });
    }
});
